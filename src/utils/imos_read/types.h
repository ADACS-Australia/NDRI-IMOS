#ifndef __TYPES_H__
#define __TYPES_H__

/* https://www.gnu.org/software/libc/manual/html_node/Integers.html
   If you require that an integer be represented in exactly N bits, 
   use one of the following types, with the obvious mapping 
   to bit size and signedness: 
*/

typedef int8_t       I8;
typedef int16_t      I16;
typedef int32_t      I32;
typedef int64_t      I64;

typedef uint8_t      U8;
typedef uint16_t     U16;
typedef uint32_t     U32;
typedef uint64_t     U64;

#endif // __TYPES_H__
